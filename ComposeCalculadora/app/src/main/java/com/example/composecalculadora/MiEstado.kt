package com.example.composecalculadora

class MiEstado {
    fun sumar(n1 : Double, n2 : Double): Double {
        return n1 + n2;
    }
    fun restar(n1 : Double, n2 : Double): Double {
        return n1 - n2;
    }
    fun multiplicar(n1 : Double, n2 : Double): Double {
        return n1 * n2;
    }
    fun dividir(n1 : Double, n2 : Double): Double {
        return n1 / n2;
    }
}